def solution(n):
    # n진법 변환의 핵심: n에서 1을 뺀 후 3으로 나누는 방식 (나머지가 0일 때 몫 조정)
    # 124 나라의 숫자는 나머지가 1, 2, 0이 아닌 1, 2, 4로 변환됩니다.
    
    answer = ''
    
    # 124 나라의 숫자에 사용되는 세 개의 문자
    # 인덱스 0, 1, 2에 각각 '4', '1', '2'를 배치하면 
    # (n % 3)의 결과에 따라 올바른 문자를 매핑할 수 있습니다.
    # 하지만 124 시스템은 n % 3이 0일 때 특수 처리가 필요하므로 
    # '4', '1', '2' 대신 '1', '2', '4'를 사용하고, n-1을 이용해 보정합니다.
    # 3진법 숫자를 1, 2, 4로 매핑
    nums = ['1', '2', '4'] 
    
    while n > 0:
        remainder = (n - 1) % 3  # 124 나라의 숫자는 n-1을 3으로 나눈 나머지를 사용
        
        answer = nums[remainder] + answer
        
        n = (n - 1) // 3 # 다음 몫을 계산할 때도 n-1을 사용
        
    return answer